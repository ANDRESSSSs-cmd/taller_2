# Valores iniciales
a = 7
b = 3

# Comparaciones
resultado_a = a != b
resultado_b = a == b
resultado_c = a > b
resultado_d = (a + b) < 2
resultado_e = (a > 7) and (b == 3)
resultado_f = (a < 7) or (b == 3)
resultado_g = (a < 7) or (b != 3)

print("Resultado de a != b:", resultado_a)
print("Resultado de a == b:", resultado_b)
print("Resultado de a > b:", resultado_c)
print("Resultado de (a + b) < 2:", resultado_d)
print("Resultado de (a > 7) and (b == 3):", resultado_e)
print("Resultado de (a < 7) or (b == 3):", resultado_f)
print("Resultado de (a < 7) or (b != 3):", resultado_g)
